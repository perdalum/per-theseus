<h1>Min fortælling om internettets historie</h1>
<em>12 marts 2014</em>

<p>
Der er i dag en del snak om at internettet har fødselsdag. Dette er en misforståelse og her er min version af internettets historie:
</p>
<p>
Den første email af den type vi kender i dag blev afsendt og modtaget i 1971 over det net der hed ARPANET[<a href="#1">[1]</a>]. ARPANET blev lavet af det amerikanske forsvar, dog ikke til krigsførelse, men til forskning på universiteterne. ARPANET kommunikerede via noget der hedder TCP/IP som er den samme teknologi der ligger under det vi i dag kalder internettet. I 1982 blev TCP/IP standardiseret og dette kan betragtes som internettes fødselsår hvorefter det langsomt spredtes til hele verden<a href="#2">[2]</a>.
</p>
<p>
I 1989 startede jeg på Århus Universitet, men jeg kan simplethen ikke huske om jeg fik en email adresse i det år eller først året efter. Under alle omstændigheder havde jeg ved slutning en af 1990 to emailadresser: pedal@kemi.aau.dk og en på DAIMI  som jeg heller ikke længere kan huske. Der var ikke andet på computerskærmene end bogstaver og tal og skulle man hente dokumenter på internettet brugte man programmer som telnet, ftp, gopher. Søgte man information var stedet Usenet med gruppenavne som comp.lang.c, alt.barefoot, dk.fritid.bil osv.
</p>

<p>
I samme år formulerede en forsker på CERN ved navn Tim Berners-Lee den geniale ide at kombinere hypertext med TCP/IP og skabte derved det vi i dag kalder The World Wide Web<a href="#3">[3]</a>. Hypertext var dog ikke noget nyt. Ordet og dets betydning stammer fra en videnskabelig artikel fra 1965<a href="#4">[4]</a> men inspirationen dertil ligger i en novelle med titlen Haven med forgrenede gange skrevet af Jorge Luis Borges og publiceret i novellesamlingen “Fiktioner”<a href="#5">[5]</a>.
</p>

<p>
Berners-Lees ide blev udbredt til det meste af verden fra 1994 og resten er nok velkendt for de fleste.
</p>

<p>
Den 25 års fødselsdag vi fejrer i dag er altså fødslen af Berners-Lees idé om hypertext over TCP/IP, altså en overbygning på eller anvendelse af internettet. Denne idé har haft umådelig stor indflydelse på den verden vi lever i her 25 år senere uden dog at være knyttet til internettet som andet end endnu en anvendelse af TCP/IP.
</p>

