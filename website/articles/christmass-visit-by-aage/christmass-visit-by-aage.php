<p>
Efter at Frederik var faldet i søvn listede jeg mig ud af sengen og ud på
gangen.  Jeg var lige ved at vække Frederiks mor, men jeg stod musestille. Så
gik jeg endnu mere stille videre ud i køkkenet. Her tryllede jeg havregrynene
om til RIS. Ti hi! Der efter tryllede jeg også sukkeret om til salt.
</p>

<p>
For lige at lave det sidste drilleri, vendte jeg deres sofabord på hovedet.
Nøj, det var tungt, men mine tryllekræfter er stærke. Måske stærkere end
Superman!
</p>

<p>
Så listede jeg mig stille tilbage til Frederiks seng og lagde mig ved siden
af ham uden at han vågnede.
</p>

<p>
Om morgenen blev hele familien meget overrasket. Først over at bordet stod på
hovedet. Så hældte de ris i tallerknen i stedet for havregryn og til sidst så de
meget underlige ud i hovedet da de spiste havregryn med salt i stedet for
sukker -- HVOR VAR DET SJOVT.
</p>

<p>
Ingen blev dog kede af det og de vidste vist godt at det bare var mig der
drillede for sjov. Frederiks far sagde godtnok at næste gang jeg kom på
besøg skulle jeg bo udenfor i kaninburet. Uha, jeg er lidt bange for store
sorte kaniner. Mon han bare drillede mig?
</p>

<p>
Alligevel er det nu skønt at være drillenisse!
</p>

