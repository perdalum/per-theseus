<h1>Julebesøg af Åge 2013</h1>

<p>
Så var dagen kommet hvor jeg skulle hjem og besøge Frederik og jeg glædede mig.
Jeg håbede virkeligt at der ville være risengrød. Jeg elsker risengrød! Jeg
virkelig ELSKER risengrød. Jeg elsker at få en kæmpe skål med dampende varm
risengrød der har en kæmpe klat smør i midten og et tykt tykt lag kanelsukker.
Det er det bedste i nissers liv. Hos Julemanden får vi det til morgenmad, til
frokost og til aftensmad. Uhm, det er godt. 
</p>

<p>
Det er bare ikke altid at jeg får det når jeg er på besøg hos Delfinbørn.
Delfinbørn får ikke risengrød til hverken morgenmad eller frokost! En gang i
mellem får de det vist til aftensmad, men vist kun om julen. Hvor er det synd
for de børn. De virker dog glade alligevel. 
</p>

<p>
Hvis JEG ikke får risengrød kan jeg ikke lade være med at drille. Jeg elsker
at drille, næsten lige så meget som jeg elsker risengrød. Julemanden siger dog
at vi kun må drille for sjov. Så DET gør jeg hvis jeg ikke får risengrød.
</p>

<p>
Fik jeg risengrød hos Frederik? NEEJ! Drillede jeg så ham og hans familie? JAA!
</p>

<p>
Frederiks familie skulle have ris til aftensmad, men ikke risengrød. Det var
bare nogle almindelige ris. Lige inden de skulle spise dem, tryllede jeg
Frederiks ris grønne! Ti hi, Frederik så meget forbavset ud! Og ved I hvad? Jeg
tryllede OGSÅ hans brors ris om så de blev helt BLÅ! Men Frederik og Sofus var
sultne så de spise tryllerisene alligevel -- måske de nu bliver grønne og blå
inde i maven, hø hø hø.
</p>

<p>
Om natten fik jeg lov til at sove sammen med Frederik og det er altid så
hyggeligt. Så fik jeg lov til at lytte med på godnathistorien. Den handlede om
at Frederik fløj til New York og mødte Superman, Batman, Hulk, Sorte
Fugl og Spiderman. Superheltene reddede det fly Frederik sad i sammen med sin
familie da det var ved at styrte ned.
</p>

